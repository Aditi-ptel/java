#include <iostream>
using namespace std;
int main (){
    int x = 4;
    cout<<x<<endl;
    x++; // post increment (add 1 to x = 5)
    cout<<x<<endl;
    x--; // post decrement (subtract 1 from x = 4)
    cout<<x<<endl;
    ++x; // pre increment 
    cout<<x<<endl;
    --x; // pre decrement
    cout<<x<<endl;
}