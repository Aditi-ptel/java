# rational or comparison operators

a = 50
b = 20

print (a == b)  # a is equal to b .      false
print (a != b)  # a is not eaqual to b .   true
print (a >= b)  # a is greater than or equal to b .    true
print (a <= b)  # a is smaller than or equal to b .    false
print (a > b)   # a is greater than b .    true 
print (a < b)   # a is smaller than b .    false