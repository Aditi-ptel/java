# assignment operators 

num = 10

num += 10   # num = num + 10,  ans = 20
print (num)

num *= 5    # num = num * 5,   ans = 20*5 = 100
print (num)

num -= 20   # num = num - 20,  ans = 100-20 = 80
print (num)

num /= 4    # num = num / 4,   ans = 80/4 = 20.0  (division always gives float value)
print (num)

num %= 3   # 20/3 karne par remainder kitna aayega  (remainder = 2.0)
print (num)

num **= 4  # num = 2.0,  2 ki power 4  ( 16 )
print (num)